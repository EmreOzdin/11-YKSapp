// İstatistik fonksiyonları
export const getQuestionStats = () => {
  const stats = {
    total: allQuestions.length,
    bySubject: {} as Record<string, number>,
    byDifficulty: {} as Record<string, number>,
    byCategory: {} as Record<string, number>,
    byYear: {} as Record<string, number>,
  };

  allQuestions.forEach(q => {
    if (q.subject) stats.bySubject[q.subject] = (stats.bySubject[q.subject] || 0) + 1;
    if (q.difficulty) stats.byDifficulty[q.difficulty] = (stats.byDifficulty[q.difficulty] || 0) + 1;
    if (q.category) stats.byCategory[q.category] = (stats.byCategory[q.category] || 0) + 1;
    if (q.examYear) stats.byYear[q.examYear.toString()] = (stats.byYear[q.examYear.toString()] || 0) + 1;
  });

  return stats;
};

